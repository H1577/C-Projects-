
#include <iostream>
using namespace std;

int main()
{
  int x = 99;
  x += 1;              //x = x + 1
  cout << x << "\n";

  short y = 11;
  y -= 1;              //y = y - 1
  cout << y << "\n";

  int z = 50;
  z *= 50;             //z = z*50
  cout << z << "\n";

  int a = 'A';
  a /= 5;              //a = a/5
  cout << a << "\n";

  int b = 'c';
  b %= -true;             //b = b%1
  cout << b << "\n";

  return 0;
}
